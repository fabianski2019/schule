Eingabe= input('Rechnung eingeben: ')
if Eingabe [1]== '+':
    print(int(Eingabe[0])+int(Eingabe[2]))
elif Eingabe [1]== '-':
    print(int(Eingabe[0])-int(Eingabe[2]))
elif Eingabe[1] == '/':
    print(int(Eingabe[0])/int(Eingabe[2]))
elif Eingabe [1]== '*':
    print(int(Eingabe[0])*int(Eingabe[2]))


